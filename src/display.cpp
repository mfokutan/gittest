#include <iostream>

using namespace std;
//============================================================================
void diplay_array(int *ptr, size_t size)
{
	for (size_t i = 0; i < size; i++) {
		cout << ptr[i];
		cout << (i < (size - 1) ? ", " : "\n\n");
	}
}
